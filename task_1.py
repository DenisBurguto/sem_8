import json


def from_txt_to_json(in_file, out_file):
    pairs = {}
    with open(in_file, 'r', encoding='utf-8') as f:
        for pair in f:
            key, value = pair.split(' | ')
            pairs[key.title()] = float(value)
    with open(out_file, 'w', encoding='utf-8') as out:
        json.dump(pairs, out, indent=1)

